/*
 * Recursively searches for "node_modules" folders starting from the specified path.
 * 
 * Parameters:
 *   - path: The path from which to start the search.
 *   - folders: Pointer to an array of NodeModulesFolder structures where the found folders will be stored.
 *   - count: Pointer to an integer representing the number of found "node_modules" folders.
 *   - totalSize: Pointer to a long integer representing the total size of all found "node_modules" folders.
 * 
 * Returns:
 *   This function does not return a value. Instead, it updates the provided 'folders', 'count', and 'totalSize' parameters.
 */
 void findNodeModulesFoldersRecursively(const char *path, NodeModulesFolder **folders, int *count, long *totalSize) {
    DIR *dir = opendir(path);
    if (dir == NULL) {
        // Error opening directory
        return;
    }

    struct dirent *entry;
    while ((entry = readdir(dir)) != NULL) {
        if (entry->d_type == DT_DIR) {
            if (strcmp(entry->d_name, ".") == 0 || strcmp(entry->d_name, "..") == 0) {
                // Ignore special dirs and . e ..
                continue;
            }

            char subdir[1024];
            sprintf(subdir, "%s/%s", path, entry->d_name);
            if (strstr(subdir, "node_modules") != NULL) {
                // Find node_modules!
                long size = calculateFolderSize(subdir);
                NodeModulesFolder *temp = realloc(*folders, (*count + 1) * sizeof(NodeModulesFolder));
                if (temp == NULL) {
                    // Memory Error
                    closedir(dir);
                    return;
                }
                *folders = temp;
                strncpy((*folders)[*count].path, subdir, sizeof((*folders)[*count].path) - 1);
                (*folders)[*count].path[sizeof((*folders)[*count].path) - 1] = '\0';
                (*folders)[*count].size = size;
                *totalSize += size;
                (*count)++;
            }
            
            // Recursively scan of subfolders
            findNodeModulesFoldersRecursively(subdir, folders, count, totalSize);
        }
    }

    closedir(dir);
}

/*
 * Function: findNodeModulesFolders
 * ------------------------------
 * Searches for "node_modules" folders recursively starting from the specified path.
 * 
 * path: The path from which to start the search.
 * 
 * Returns: void
 */
void findNodeModulesFolders(const char *path) {
    printf("Search for 'node_modules' folders ...\n");

    // Dynamic memory alloc for array
    NodeModulesFolder *folders = NULL;
    int count = 0;
    long totalSize = 0;

    // Recursively folder scan
    findNodeModulesFoldersRecursively(path, &folders, &count, &totalSize);

    // Print folder with dimensions
    printf("List of 'node_modules' folders found:\n");
    for (int i = 0; i < count; i++) {
        printf("Folder: %s (dimension: %ld bytes)\n", folders[i].path, folders[i].size);
    }

    // Print total dimensions of folders
    printf("Total maximum size of the 'node_modules' folders found: %ld bytes\n", totalSize);

    // free memory
    free(folders);
}

/*
 * Calculate the size of a folder recursively.
 * 
 * Parameters:
 *   - path: The path of the folder whose size is to be calculated.
 * 
 * Returns:
 *   - The size of the folder in bytes.
 * 
 * Note:
 *   - If the specified path does not point to a valid folder or an error occurs during calculation,
 *     the function returns -1.
 */
long calculateFolderSize(const char *path) {
    long size = 0;
    struct dirent *entry;
    DIR *dir = opendir(path);

    if (dir == NULL) {
        return -1;
    }

    while ((entry = readdir(dir)) != NULL) {
        if (entry->d_type == DT_REG) {
            struct stat statbuf;
            char filepath[1024];
            sprintf(filepath, "%s/%s", path, entry->d_name);
            if (stat(filepath, &statbuf) == 0) {
                size += statbuf.st_size;
            }
        } else if (entry->d_type == DT_DIR && strcmp(entry->d_name, ".") != 0 && strcmp(entry->d_name, "..") != 0) {
            char subdir[1024];
            sprintf(subdir, "%s/%s", path, entry->d_name);
            size += calculateFolderSize(subdir);
        }
    }

    closedir(dir);
    return size;
}

/*
 * Function: deleteNodeModulesFolders
 * ------------------------------
 * This function recursively searches for and deletes all "node_modules" folders
 * within the specified directory and its subdirectories.
 *
 * path: The path of the directory from which to start the search.
 *
 * Returns: void
 */
void deleteNodeModulesFolders(const char *path) {
    // Otteniamo le cartelle 'node_modules'
    NodeModulesFolder *folders = NULL;
    int count = 0;
    long totalSize = 0;
    findNodeModulesFoldersRecursively(path, &folders, &count, &totalSize);

    // Eliminiamo ciascuna cartella 'node_modules'
    for (int i = 0; i < count; i++) {
        char *folderPath = folders[i].path;
        if (rmdir(folderPath) == -1) {
            if (errno == ENOTEMPTY) {
                // La cartella non Ã¨ vuota, quindi eseguiamo una rimozione ricorsiva
                if (removeDirectoryRecursive(folderPath) == 0) {
                    printf("Folder '%s' deleted.\n", folderPath);
                } else {
                    printf("Error while deleting the folder '%s'.\n", folderPath);
                }
            } else {
                printf("Error while deleting the folder '%s'.\n", folderPath);
            }
        } else {
            printf("Folder '%s' deleted.\n", folderPath);
        }
    }

    // Liberiamo la memoria allocata per l'array di cartelle
    free(folders);
}

/*
 * Recursively removes the directory specified by the given path.
 * 
 * Parameters:
 *   - path: The path of the directory to be removed.
 * 
 * Returns:
 *   - 0 if the directory is successfully removed.
 *   - -1 if an error occurs during the removal process.
 */
int removeDirectoryRecursive(const char *path) {
    DIR *d = opendir(path);
    size_t path_len = strlen(path);
    int r = -1;

    if (d) {
        struct dirent *p;
        r = 0;
        while (!r && (p = readdir(d))) {
            int r2 = -1;
            char *buf;
            size_t len;
            
            // Ignora le directory speciali . e ..
            if (!strcmp(p->d_name, ".") || !strcmp(p->d_name, "..")) {
                continue;
            }

            len = path_len + strlen(p->d_name) + 2;
            buf = malloc(len);
            
            if (buf) {
                struct stat statbuf;
                snprintf(buf, len, "%s/%s", path, p->d_name);
                if (!stat(buf, &statbuf)) {
                    if (S_ISDIR(statbuf.st_mode)) {
                        r2 = removeDirectoryRecursive(buf);
                    } else {
                        r2 = unlink(buf);
                    }
                }
                free(buf);
            }
            r = r2;
        }
        closedir(d);
    }

    if (!r) {
        r = rmdir(path);
    }
    return r;
}
